    public void autoID()
    {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/miniproject","root","");
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("select Max(car_no) from car_reg");
            rs.next();
            rs.getString("Max(car_no)");
            if(rs.getString("Max(car_no)")== null)
            {
                txtregno.setText("C0001");
            }
            else
            {
                long id= Long.parseLong(rs.getString("Max(car_no)").substring(2,rs.getString("Max(car_no)").length()));
                id++;
                txtregno.setText("C0"+ String.format("%03d", id));
                
            }
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(taxireg.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(taxireg.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
    
